void keyboard_post_init_user(void) {
    setPinInputHigh(GP15); // Botão no GP15
}

void matrix_scan_user(void) {
    static bool button_pressed = false;
    static uint16_t debounce_timer = 0;
    
    // Lê estado do botão (ativo LOW se usas pull-up)
    bool button_state = !readPin(GP15);
    
    // Debounce de 25ms
    if (button_state != button_pressed) {
        if (timer_elapsed(debounce_timer) > 25) {
            if (button_state) { // Botão premido AGORA
                // MUDAR PARA PRÓXIMA LAYER
                uint8_t current_layer = get_highest_layer(layer_state);
                uint8_t next_layer = (current_layer + 1) % _LAYER_COUNT;
                layer_move(next_layer);
            }
            button_pressed = button_state;
            debounce_timer = timer_read();
        }
    }
}

